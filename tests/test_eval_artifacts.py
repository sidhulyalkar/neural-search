"""Regression checks for artifact-backed whitepaper tables."""
from __future__ import annotations

import csv
import json
from collections import defaultdict
from pathlib import Path

REPORTS = Path("reports/eval")
GENERATED_TABLES = Path("docs/whitepaper/generated/current_results_tables.tex")


def _read_csv(path: Path) -> list[dict[str, str]]:
    with path.open(encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def test_source_distribution_sums_to_corpus_size():
    manifest = json.loads((REPORTS / "corpus_manifest.json").read_text(encoding="utf-8"))
    source_rows = _read_csv(REPORTS / "source_distribution.csv")

    assert sum(int(row["records"]) for row in source_rows) == manifest["corpus_size"]
    assert sum(manifest["source_distribution"].values()) == manifest["corpus_size"]


def test_field_completeness_report_matches_corpus_records():
    manifest = json.loads((REPORTS / "corpus_manifest.json").read_text(encoding="utf-8"))
    rows = _read_csv(REPORTS / "field_completeness.csv")
    totals_by_field: dict[str, int] = defaultdict(int)

    for row in rows:
        totals_by_field[row["field"]] += int(row["total"])

    assert totals_by_field
    assert all(total == manifest["corpus_size"] for total in totals_by_field.values())


def test_generated_tables_use_current_artifact_values():
    manifest = json.loads((REPORTS / "corpus_manifest.json").read_text(encoding="utf-8"))
    tables = GENERATED_TABLES.read_text(encoding="utf-8")

    assert "% Auto-generated by scripts/eval/generate_paper_tables.py" in tables
    assert f"{manifest['corpus_size']:,}" in tables
    assert "End-to-end 10K retrieval performance" in tables
    assert "Pending" in tables
