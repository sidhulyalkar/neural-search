#!/usr/bin/env python3
"""Generate current whitepaper tables from reproducibility artifacts."""
from __future__ import annotations

import argparse
import csv
import json
from pathlib import Path

DEFAULT_REPORTS = Path("reports/eval")
DEFAULT_OUT = Path("docs/whitepaper/generated/current_results_tables.tex")


def latex_escape(value: object) -> str:
    text = str(value)
    return (
        text.replace("\\", "\\textbackslash{}")
        .replace("&", "\\&")
        .replace("%", "\\%")
        .replace("_", "\\_")
    )


def display_value(value: object) -> object:
    if isinstance(value, int):
        return f"{value:,}"
    return value


def read_csv(path: Path) -> list[dict[str, str]]:
    if not path.exists():
        return []
    with path.open(encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def load_json(path: Path) -> dict:
    if not path.exists():
        return {}
    return json.loads(path.read_text(encoding="utf-8"))


def begin_table(caption: str, columns: str, header: str) -> list[str]:
    return [
        "\\begin{table}[htbp]",
        "\\centering",
        f"\\caption{{{caption}}}",
        "\\small",
        f"\\begin{{tabular}}{{{columns}}}",
        "\\toprule",
        header + " \\\\",
        "\\midrule",
    ]


def end_table() -> list[str]:
    return ["\\bottomrule", "\\end{tabular}", "\\end{table}", ""]


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--reports-dir", type=Path, default=DEFAULT_REPORTS)
    parser.add_argument("--out", type=Path, default=DEFAULT_OUT)
    args = parser.parse_args(argv)

    manifest = load_json(args.reports_dir / "corpus_manifest.json")
    turbovec = load_json(Path("reports/turbovec_recall.json"))
    usefulness = load_json(Path("reports/usefulness_correlation_v09.json"))
    graph = load_json(Path("reports/graph_ablation.json"))
    source_rows = read_csv(args.reports_dir / "source_distribution.csv")
    completeness_rows = read_csv(args.reports_dir / "field_completeness.csv")
    rejection_rows = read_csv(args.reports_dir / "rejection_summary.csv")

    lines: list[str] = [
        "% Auto-generated by scripts/eval/generate_paper_tables.py",
        "% Do not hand-edit numeric values in this file.",
        "",
    ]

    lines.extend(begin_table("Current corpus and index artifact summary.", "lc", "Metric & Value"))
    rows = [
        ("Unique normalized records", manifest.get("corpus_size", "Pending benchmark artifact")),
        ("Dense field embeddings", manifest.get("embedding_rows", "Pending benchmark artifact")),
        ("Embedding model", manifest.get("embedding_model", "Pending benchmark artifact")),
        ("Embedding dimension", manifest.get("embedding_dimension", "Pending benchmark artifact")),
        ("Indexed ids", manifest.get("index_size", "Pending benchmark artifact")),
        ("Index bit width", manifest.get("index_bit_width", "Pending benchmark artifact")),
        ("Vector index recall@50", turbovec.get("mean_recall", "Pending benchmark artifact")),
        ("Vector index latency p50 / p95", f"{turbovec.get('p50_latency_ms', 'Pending')} / {turbovec.get('p95_latency_ms', 'Pending')} ms"),
        ("Tier-2 rejections logged", sum(int(row.get("count", 0)) for row in rejection_rows) if rejection_rows else "Pending benchmark artifact"),
    ]
    for metric, value in rows:
        lines.append(f"{latex_escape(metric)} & {latex_escape(display_value(value))} \\\\")
    lines.extend(end_table())

    if source_rows:
        lines.extend(begin_table("Current source distribution.", "lr", "Source & Records"))
        for row in source_rows:
            lines.append(f"{latex_escape(row['source'])} & {int(row['records']):,} \\\\")
        lines.extend(end_table())

    if completeness_rows:
        totals: dict[str, tuple[int, int]] = {}
        for row in completeness_rows:
            field = row["field"]
            non_empty = int(row["non_empty"])
            total = int(row["total"])
            old_non_empty, old_total = totals.get(field, (0, 0))
            totals[field] = (old_non_empty + non_empty, old_total + total)
        lines.extend(begin_table("Field completeness on the current corpus.", "lrr", "Field & Non-empty & Coverage"))
        for field, (non_empty, total) in totals.items():
            coverage = f"{100 * non_empty / total:.1f}\\%" if total else "0.0\\%"
            lines.append(f"{latex_escape(field)} & {non_empty:,} & {coverage} \\\\")
        lines.extend(end_table())

    lines.extend(begin_table("Claims versus current evidence.", "@{}p{0.22\\linewidth}p{0.25\\linewidth}p{0.15\\linewidth}p{0.28\\linewidth}@{}", "Claim & Evidence artifact & Current status & Risk / next validation"))
    claim_rows = [
        ("Corpus scale", "corpus_manifest.json", "Supported", "Freeze manifest with commit/hash metadata"),
        ("Dense index recall/latency", "reports/turbovec_recall.json", "Index validated", "Not an end-to-end relevance metric"),
        ("Usefulness scorer correlation", "reports/usefulness_correlation_v09.json", "Preliminary", "Needs independent labels"),
        ("Graph rank perturbation", "reports/graph_ablation.json", "Rank behavior changed", "Relevance improvement unproven"),
        ("End-to-end 10K retrieval performance", "real_corpus_10k_eval_report.json", "Pending", "Requires frozen benchmark"),
        ("Content-validated analysis affordances", "affordance_validation_report.json", "Pending", "Requires file inspection"),
        ("Confidence calibration", "calibration_report.json", "Pending", "Requires ECE on labeled benchmark"),
    ]
    for row in claim_rows:
        lines.append(" & ".join(latex_escape(cell) for cell in row) + " \\\\")
    lines.extend(end_table())

    lines.extend(begin_table("Current usefulness and graph validation artifacts.", "lc", "Artifact & Value"))
    validation_rows = [
        ("Usefulness Spearman r", usefulness.get("spearman_r", "Pending benchmark artifact")),
        ("Usefulness labeled pairs", usefulness.get("n_pairs", "Pending benchmark artifact")),
        ("Mean useful score / irrelevant score", f"{usefulness.get('mean_score_relevant', 'Pending')} / {usefulness.get('mean_score_irrelevant', 'Pending')}"),
        ("Graph pairs changed", f"{graph.get('pct_pairs_changed', 'Pending benchmark artifact')}%"),
        ("10K retrieval benchmark", "Pending benchmark artifact"),
    ]
    for metric, value in validation_rows:
        lines.append(f"{latex_escape(metric)} & {latex_escape(value)} \\\\")
    lines.extend(end_table())

    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text("\n".join(lines), encoding="utf-8")
    print(args.out)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
